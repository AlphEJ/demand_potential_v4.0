// 需求关联矩阵（对称矩阵，表示协同/冲突关系 -1~1）
export function buildAssociationMatrix(): number[][] {
  const M: number[][] = NEED_NAMES.map(() => NEED_NAMES.map(() => 0))
  // 手动设置关键关系
  const pairs: [number, number, number][] = [
    // 强协同
    [0, 1, 0.5], // 生理 ↔ 安全
    [2, 3, 0.6], // 社交 ↔ 情感
    [4, 7, 0.4], // 尊重 ↔ 自我实现
    [5, 10, 0.7], // 认知 ↔ 成长
    [7, 10, 0.6], // 自我实现 ↔ 成长
    [7, 8, 0.5], // 自我实现 ↔ 自由
    [10, 11, 0.5], // 成长 ↔ 秩序
    [5, 11, 0.4], // 认知 ↔ 秩序
    // 弱协同
    [0, 2, 0.2], [2, 4, 0.3], [4, 5, 0.3], [5, 6, 0.3], [6, 7, 0.4],
    [3, 4, 0.2], [1, 11, 0.3], [2, 10, 0.2], [3, 7, 0.3],
    // 冲突
    [1, 8, -0.5], // 安全 ↔ 自由（典型冲突）
    [10, 0, -0.3], // 成长 ↔ 生理（奋斗消耗身体）
    [8, 11, -0.4], // 自由 ↔ 秩序（随性 vs 规矩）
    [7, 1, -0.3], // 自我实现 ↔ 安全（追梦可能牺牲稳定）
    [4, 9, 0.3], // 尊重 ↔ 公平
    [8, 1, -0.5], // 自由掌控 ↔ 安全稳定
  ]
  for (const [a, b, v] of pairs) {
    M[a][b] = v
    M[b][a] = v
  }
  for (let i = 0; i < 12; i++) M[i][i] = 1.0
  return M
}
